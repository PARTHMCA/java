package over;

//Write a program in Java to display the first 
//10 natural numbers using while loop

public class p44 {
	
public static void main(String[] args) {
	int i = 1;
	System.out.println ("The first 10 natural numbers are:\n");
	while( i <= 10) {
		System.out.print( i + " " );
		i++;
 }
}
}